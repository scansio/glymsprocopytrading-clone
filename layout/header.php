    // Function to toggle the user menu
    function toggleUserMenu() {
        const userMenu = document.getElementById('userMenu');
        if (userMenu.style.display === 'none' || userMenu.style.display === '') {
            userMenu.style.display = 'block';
        } else {
            userMenu.style.display = 'none';
        }
    }

    // Function to toggle the notifications panel
    function toggleNotifications() {
        const slideInRoot = document.getElementById('slideIn-root');
        if (slideInRoot.style.display === 'none' || slideInRoot.style.display === '') {
            slideInRoot.style.display = 'block';
            setTimeout(() => {
                slideInRoot.style.transform = 'initial';
            }, 10);
        } else {
            slideInRoot.style.transform = 'translateX(100%)';
            setTimeout(() => {
                slideInRoot.style.display = 'none';
            }, 300); // Match this duration with your CSS transition duration
        }
    }

